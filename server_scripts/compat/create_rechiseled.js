ServerEvents.tags('item', event => {
  rechiseledTarget.forEach(rechiseledTargetItem => {
    event.add(
      `rechiseled:${rechiseledTargetItem.original}`,
      `minecraft:${rechiseledTargetItem.original}`
    )
    rechiseledTargetItem.variants.forEach(e => {
      event.add(
        `rechiseled:${rechiseledTargetItem.original}`,
        `rechiseled:${e}`
      )
    })
  })
})

